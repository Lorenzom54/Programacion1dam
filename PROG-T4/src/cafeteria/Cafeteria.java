package cafeteria;

public class Cafeteria {
    //Atributos

    String capacidadMaxima;

    String cantidadActual;

    //Constructores

    public Cafeteria(){}

    public Cafeteria(String capacidadMaxima) {
        this.capacidadMaxima = capacidadMaxima;
    }

    public Cafeteria(String capacidadMaxima, String cantidadActual) {
        this.capacidadMaxima = capacidadMaxima;
        this.cantidadActual = cantidadActual;
    }

    //Metodos



}
